import UIKit

// MARK: - Format

extension Int {
    
    /**  */
    var double: Double { return Double(self) }
    
    /**  */
    var float: Float { return Float(self) }
    
    /**  */
    var cgfloat: CGFloat { return CGFloat(self) }
    
}
